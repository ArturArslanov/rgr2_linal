def test_non_square_matrix_rejected():
    A = sp.Matrix([
        [1, 2, 3],
        [4, 5, 6],
    ])

    with pytest.raises(ValueError):
        jordan_form_exact(A)



def test_transition_matrix_is_invertible_for_recovered_decomposition():
    A = sp.Matrix([
        [0, 2],
        [1, 0],
    ])

    J, P, _ = jordan_form_exact(A)
    assert P.det() != 0
    assert_valid_jordan_decomposition(A, J, P)
