"""
Pytest tests for jordan_form_sympy_exact.py.

Run:
    pip install pytest sympy
    pytest -q

Expected project structure:
    jordan_form_sympy_exact.py
    test_jordan_form_sympy_exact.py

These tests intentionally do not use eigenvals/eigenvects/jordan_form/SVD.
They verify the implemented Jordan algorithm through exact identities:
    A * P == P * J
    det(P) != 0
    characteristic polynomials agree
    recovered Jordan block structure matches the expected one
"""

import pytest
import sympy as sp

from main import (
    jordan_form_exact,
    make_jordan_matrix,
    random_unimodular_matrix,
    same_jordan_structure,
    algebraic_roots_with_multiplicities,
    jordan_block_sizes,
)


# =========================================================
# Helpers
# =========================================================


def assert_zero_matrix(M):
    M = sp.simplify(M)
    assert M == sp.zeros(M.rows, M.cols)



def assert_valid_jordan_decomposition(A, J, P):
    """
    Checks the main contract:
        A * P = P * J
        det(P) != 0
    """
    A = sp.Matrix(A)
    J = sp.Matrix(J)
    P = sp.Matrix(P)

    assert P.det() != 0
    assert_zero_matrix(A * P - P * J)



def expected_structure_from_block_specs(block_specs):
    """
    Converts [(lambda, size), ...] into
    [(lambda, [sizes...]), ...]
    """
    result = []

    for lam, size in block_specs:
        inserted = False
        for i, (old_lam, sizes) in enumerate(result):
            if sp.simplify(old_lam - lam) == 0:
                sizes.append(size)
                inserted = True
                break

        if not inserted:
            result.append((lam, [size]))

    for _, sizes in result:
        sizes.sort(reverse=True)

    result.sort(key=lambda item: str(item[0]))
    return result



def structure_from_J(J):
    """
    Extracts Jordan block structure directly from a Jordan matrix J.

    Assumes standard Jordan form with eigenvalues on diagonal and ones
    on the superdiagonal inside blocks.
    """
    J = sp.Matrix(J)
    n = J.rows
    result = []
    i = 0

    while i < n:
        lam = sp.simplify(J[i, i])
        size = 1

        while i + size < n and sp.simplify(J[i + size - 1, i + size]) == 1:
            if sp.simplify(J[i + size, i + size] - lam) != 0:
                break
            size += 1

        found = False
        for idx, (old_lam, sizes) in enumerate(result):
            if sp.simplify(old_lam - lam) == 0:
                sizes.append(size)
                found = True
                break

        if not found:
            result.append((lam, [size]))

        i += size

    for _, sizes in result:
        sizes.sort(reverse=True)

    result.sort(key=lambda item: str(item[0]))
    return result



def run_generated_case(block_specs, seed=0):
    """
    Generates exact test matrix:
        A = P0 * J0 * P0^{-1},
    where P0 is unimodular.
    Then verifies recovered J and P.
    """
    J0 = make_jordan_matrix(block_specs)
    P0 = random_unimodular_matrix(J0.rows, seed=seed)
    A = sp.simplify(P0 * J0 * P0.inv())

    J, P, recovered_structure = jordan_form_exact(A)

    expected_structure = expected_structure_from_block_specs(block_specs)

    assert_valid_jordan_decomposition(A, J, P)
    assert same_jordan_structure(expected_structure, recovered_structure)
    assert same_jordan_structure(expected_structure, structure_from_J(J))

    # Similar matrices have the same characteristic polynomial.
    t = sp.Symbol("x")
    assert sp.factor(A.charpoly(t).as_expr() - J.charpoly(t).as_expr()) == 0


