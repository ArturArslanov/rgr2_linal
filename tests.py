def test_roots_with_multiplicities_integer_repeated():
    A = make_jordan_matrix([(2, 3), (-1, 2)])

    roots = algebraic_roots_with_multiplicities(A)
    expected = [(-1, 2), (2, 3)]

    assert len(roots) == len(expected)
    for (got_lam, got_mult), (exp_lam, exp_mult) in zip(roots, expected):
        assert sp.simplify(got_lam - exp_lam) == 0
        assert got_mult == exp_mult



def test_roots_with_multiplicities_irrational_pair():
    A = sp.Matrix([
        [0, 2],
        [1, 0],
    ])

    roots = algebraic_roots_with_multiplicities(A)
    expected = [(-sp.sqrt(2), 1), (sp.sqrt(2), 1)]

    assert len(roots) == len(expected)
    for (got_lam, got_mult), (exp_lam, exp_mult) in zip(roots, expected):
        assert sp.simplify(got_lam - exp_lam) == 0
        assert got_mult == exp_mult



def test_block_sizes_one_maximal_nilpotent_block():
    A = make_jordan_matrix([(0, 4)])
    sizes = jordan_block_sizes(A, 0, 4)
    assert sizes == [4]



def test_block_sizes_multiple_blocks_same_lambda():
    A = make_jordan_matrix([(5, 3), (5, 2), (5, 1)])
    sizes = jordan_block_sizes(A, 5, 6)
    assert sizes == [3, 2, 1]


