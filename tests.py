"""
We test the following situations:

1. Diagonalizable matrices with distinct integer eigenvalues.
2. Diagonalizable matrices with irrational algebraic eigenvalues.
3. A nilpotent matrix with one maximal Jordan block J_n(0).
4. A matrix with one maximal Jordan block J_n(lambda), lambda != 0.
5. Several Jordan blocks for the same eigenvalue.
6. Mixed spectrum: integer and algebraic irrational eigenvalues.
7. Matrices not initially in Jordan form, generated by A = PJP^{-1}.
8. Correct recovery of characteristic roots and multiplicities.
9. Correct recovery of block sizes from defects dim Ker((A-lambda I)^k).
10. Generator correctness: random_unimodular_matrix must have determinant +-1.
"""


# =========================================================
# Basic Jordan decomposition tests
# =========================================================


def test_diagonalizable_distinct_integer_eigenvalues():
    block_specs = [(1, 1), (2, 1), (3, 1)]
    run_generated_case(block_specs, seed=1)



def test_diagonalizable_irrational_eigenvalues_sqrt2_pair():
    A = sp.Matrix([
        [0, 2],
        [1, 0],
    ])

    J, P, recovered_structure = jordan_form_exact(A)

    expected_structure = [(-sp.sqrt(2), [1]), (sp.sqrt(2), [1])]

    assert_valid_jordan_decomposition(A, J, P)
    assert same_jordan_structure(expected_structure, recovered_structure)
    assert same_jordan_structure(expected_structure, structure_from_J(J))



def test_nilpotent_one_maximal_block_j3_zero():
    block_specs = [(0, 3)]
    run_generated_case(block_specs, seed=2)



def test_one_maximal_block_j3_lambda_2():
    block_specs = [(2, 3)]
    run_generated_case(block_specs, seed=3)



def test_two_blocks_same_eigenvalue():
    block_specs = [(3, 2), (3, 1)]
    run_generated_case(block_specs, seed=4)



def test_mixed_integer_and_irrational_spectrum():
    block_specs = [(sp.sqrt(2), 1), (-sp.sqrt(2), 1), (3, 1)]
    run_generated_case(block_specs, seed=5)



def test_symbolic_jordan_block_for_sqrt2():
    # This matrix has entries in Q(sqrt(2)), not only integers.
    # It tests exact algebraic arithmetic and Jordan chains for algebraic lambda.
    block_specs = [(sp.sqrt(2), 2)]
    run_generated_case(block_specs, seed=6)


# =========================================================
# Direct tests for roots and block sizes
